package tictactoe.beans;

public class Statistic {

	private Integer nVinte = 0;
	private Integer nPerse = 0;
	private Integer nPari = 0;
	
	public Statistic(){

	}

	public Integer getnVinte() {
		return nVinte;
	}

	public void setnVinte(Integer nVinte) {
		this.nVinte = nVinte;
	}

	public Integer getnPerse() {
		return nPerse;
	}

	public void setnPerse(Integer nPerse) {
		this.nPerse = nPerse;
	}

	public Integer getnPari() {
		return nPari;
	}

	public void setnPari(Integer nPari) {
		this.nPari = nPari;
	}


}
